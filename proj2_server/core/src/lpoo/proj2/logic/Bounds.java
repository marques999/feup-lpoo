package lpoo.proj2.logic;

public class Bounds
{
	protected float minX;
	protected float maxX;
	protected float minY;
	protected float maxY;

	public Bounds(final float minX, final float minY, final float maxX, final float maxY)
	{
		this.minX = minX;
		this.minY = minY;
		this.maxX = maxX;
		this.maxY = maxY;
	}
}